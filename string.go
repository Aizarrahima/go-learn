package main

import "fmt"

func main() {
	fmt.Println("Aizar")
	fmt.Println("Aizar Rahima")
	fmt.Println("Aizar Rahima Suprayitno")

	fmt.Println(" ")

	fmt.Println(len("Aizar")) // len menghitung jumlah karakter pada string
	fmt.Println("Aizar Rahima"[0]) // 65 merupakan representasi dari byte huruf "A"
	fmt.Println("Aizar Rahima Suprayitno"[1]) // 105 merupakan representasi dari byte huruf "i"
}